package com.pss.exercise.model.vo;

public class ExerciseInfo {
	private String exSet;
	private String exWeight;
	private String exCount;
	
	public String getExSet() {
		return exSet;
	}
	public void setExSet(String exSet) {
		this.exSet = exSet;
	}
	public String getExWeight() {
		return exWeight;
	}
	public void setExWeight(String exWeight) {
		this.exWeight = exWeight;
	}
	public String getExCount() {
		return exCount;
	}
	public void setExCount(String exCount) {
		this.exCount = exCount;
	}
	
	
	
}
